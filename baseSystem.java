public class baseSystem {
    public static void main(String[] args) {
        //进制   二进制0b   十进制   八进制0     十六进制0x
        int i = 10;
        int i2 = 010;
        int i3 = 0x10;
        System.out.println(i);
        System.out.println(i2);   //八进制
        System.out.println(i3);    //十六进制
    }
}
